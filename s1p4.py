# Define the polynomial p(x) = x^5 - 3x^3 + 3x^2 - x + 2
def p(x):
    return x**5 - 3*x**3 + 3*x**2 - x + 2

# Use the Remainder Theorem: remainder = p(2)
remainder = p(2)

# Output the result
print("The remainder of dividing p(x) by x - 2 is:", remainder)



